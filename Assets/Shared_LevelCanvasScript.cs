using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Shared_LevelCanvasScript : MonoBehaviour {

    public static Shared_LevelCanvasScript Instance;

    private void Awake()
    {
        if (Instance == null)
        {
            DontDestroyOnLoad(gameObject);
            Instance = this;
        }
        else if (Instance != this)
        {
            Destroy(gameObject);
        }
    }

    // Use this for initialization
    void Start () {
		
	}
	
	// Update is called once per frame
	void Update () {
		
	}
}
